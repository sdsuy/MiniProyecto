package semana3;

public class Club {

	//Atributos	
		private String nombre;
		private String direccion;
		private int telefono;
		private String web;
	//Constructor
	public Club(String nombre, String direccion, int telefono, String web) {
			
			this.nombre = nombre;
			this.direccion = direccion;
			this.telefono = telefono;
			this.web = web;
		
	}
	
	
}
